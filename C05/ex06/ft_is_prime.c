int	ft_is_prime(int nb)
{
	int	x;
	int	y;

	y = 0;
	x = 1;
	if (nb == 0 || nb == 1)
		return (0);
	else if (nb < 0)
		return (0);
	while (x <= nb)
	{
		if (nb % x == 0)
			y++;
		x++;
	}	
	if (y == 2)
		return (1);
	else
		return (0);
}
