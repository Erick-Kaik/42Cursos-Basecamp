int	ft_strcmp(char *s1, char *s2)
{
	int	x;
	int	result;

	x = 0;
	result = 0;
	while (s1[x] != '\0' || s2[x] != '\0')
	{
		if (s1[x] != s2[x])
		{
			result = s1[x] - s2[x];
			break ;
		}
		x++;
	}
	return (result);
}
